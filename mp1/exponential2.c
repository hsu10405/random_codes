#include <stdio.h>
#include <stdlib.h>

/*C program to approximate the function e^x*/

int main()
{
    /*  Variable definitions.
    Variable x will contain the value of x from the user and
    Variable ans will have the final calculated answer
    (DO NOT CHANGE THE VARIABLE NAMES)
    */
    double x;
    double ans;

    /*Ask user to enter in the value of x (DO NOT CHANGE THE FOLLOWING 2 STATEMENTS)*/
    printf("Enter in the value of x:");
    scanf("%lf",&x);

    /*Calculate the 5 terms and add to the ans*/
    

	ans = 1 + x + ((x * x) / 2) + ((x * x * x) / (2 * 3)) + ((x * x * x * x) / (4 * 3 * 2));




    /*Display the answer in vaiable ans (DO NOT CHANGE THE FOLLOWING STATEMENT)*/
    printf("Answer = %lf\n",ans);

    return 0;
}	

   
    
